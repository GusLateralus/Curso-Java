/*
Ejercicio 1: Realizar un programa que muestre en pantalla los números del 1 al 35
(uno abajo del otro). Utilizar una estructura repetitiva para esto.
*/
package ejercicio1_bucles;


public class Ejercicio1_Bucles {

    
    public static void main(String[] args) {
        System.out.println("Lista de números: ");
        
        for(int i = 1;i <= 35; i++)
        {
            System.out.println(" "+i);
        }
    }
    
}
